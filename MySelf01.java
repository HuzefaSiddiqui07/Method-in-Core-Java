// How to return using Parameterized Constructor

package com.method2;

public class MySelf01 {
	// Declaration
	String myName;
	String myAddress;
	char myGender;
	int myAge;
	double myWeight;
	long myContactNumber;

	// Create Parameterized Constructor
	public MySelf01(String myName, String myAddress, char myGender, int myAge, double myWeight, long myContactNumber) {

		this.myName = myName;
		this.myAddress = myAddress;
		this.myGender = myGender;
		this.myAge = myAge;
		this.myWeight = myWeight;
		this.myContactNumber = myContactNumber;
	}
}
