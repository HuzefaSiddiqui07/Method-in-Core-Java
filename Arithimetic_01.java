// How to return using Parameterized Constructor
package com.method2;

public class Arithimetic_01 {
	// Declaration
	int v;
	int c;
	int add;
	int subtract;
	int multiply;

	// Create Parameterized Constructor
	public Arithimetic_01(int v, int c, int add, int subtract, int multiply) {
		this.v = v;
		this.c = c;
		this.add = add;
		this.subtract = subtract;
		this.multiply = multiply;
	}

}
